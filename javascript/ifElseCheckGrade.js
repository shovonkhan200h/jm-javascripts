let numer= 39;

if(numer >=80 && numer <=100){
    console.log(`A+`)
}

else if(numer >=70 && numer <=79){
    console.log(`A-`)
}

else if(numer >=60 && numer <=69){
    console.log(`B`)
}

else if(numer >=50 && numer <=59){
    console.log(`C`)
}

else if(numer >=40 && numer <=40){
    console.log(`Pass`)
}
else{
    console.log(`Failed`)
}







